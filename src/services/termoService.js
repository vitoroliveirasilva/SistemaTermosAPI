const BaseService = require('./baseService');
const termoRepository = require('../repositories/termoRepository');
const {
    validarUnicidadeTermo
} = require('../utils');

class TermoService extends BaseService {
    constructor() {
        super(termoRepository, 'Termo');
    }

    async criar(dados) {
        await this.#validarUnicidade(dados);
        return super.criar(dados);
    }

    async atualizar(id, dados) {
        await this.#validarUnicidade(dados);
        return super.atualizar(id, dados);
    }

    async #validarUnicidade(dados) {
        const errosValidacao = await validarUnicidadeTermo(dados);
        if (errosValidacao.length > 0) {
            throw {
                status: 400,
                message: errosValidacao.length > 1 ? 'Campos duplicados.' : 'Campo duplicado.',
                errors: errosValidacao
            };
        }
    }
}

module.exports = new TermoService();